//There are different methods that output JavaScript statements such as alert, prompt
//Alert
function plus(a,b){
    return a+b;
}

alert(plus(10,10));

//Prompt
prompt("What is your name?", "Name:");

console.log(prompt("What is your name?", "Name:"));

//Confirm
// Method usually outputs Boolean values
console.log(confirm("The fireplace is cozy"));

//console
console.log("Can i order a cup of coffee?");
console.warn("System failure");
console.error("Syntax error detected");

//Console is preferred to alert because is gives proper code output details including code errors.